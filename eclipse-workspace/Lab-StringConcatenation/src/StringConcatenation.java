
public class StringConcatenation {

	public static void main(String[] args) {
		
		//Concatenate Strings
		String beginning = "Hello";
		String end = " World";
		String combo = beginning + end;
		
		System.out.println(combo);
		
		//Concatenate Primitive numbers
		long l = 203L;
		System.out.println("The value of l: " + l);
		
		//Concatenate boolean values
		boolean b = true;
		System.out.println(b + " is true");
		
		//Concatenate two numbers and a string
		int x = 2;
		int y = 3;
		System.out.println(x + y + " is the sum of x and y");

	}

}
